import java.util.ArrayList;
import java.util.List;

public class Inheritance2 {
	static void print(ArrayList<String> list2) {
		for (String str : list2) {
			System.out.println(str);
		}
	}
	
	static void print2(List<String> list2) {
		for (String str : list2) {
			System.out.println(str);
		}
	}
}
